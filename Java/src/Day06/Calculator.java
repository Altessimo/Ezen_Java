package Day06;

public class Calculator {
// 필드
// 생성자
	// 메소드
	void powerOn() {
		System.out.println("전원을 켭니다");
	}
	// 인수 O, 변환 O
	int plus(int x, int y) {
		int result = x+y;
		return result;
	}
	// 인수 O, 변환 O
	double dicide(int x, int y) {
		double result = (double) x / (double) y;
		return result;
	}
	// 인수 X, 반환X(void)
	void poweroff() {
		System.out.println("전원을 끕니다");
	}
}
